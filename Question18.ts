let places : string[] = ["Italy","Newzealand","Australia","Japan","Mexico"]
let sortArr : string[] = [...places].sort()

console.log(places)//Printing original array
console.log(sortArr)//Printing sorted array
console.log(places)//Printing original array
console.log(sortArr.reverse())//Printing sorted array in reverse
console.log(places)//Printing original array
console.log(places.reverse())//Printing original array in reverse
console.log(places.reverse())//Printing original reversed array in reverse
console.log(places.sort())//Sorting array again
console.log(places.sort().reverse())//Printing sorte array in reverse
